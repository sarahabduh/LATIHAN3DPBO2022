package Latihan3;

class Disk {
    private String type;
    private int capacity;
    private int price;

    // Constructor
    public Disk(){
    }

    // Get-set Type
    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
    
    // Get-set Capacity
    public int getCapacity() {
        return capacity;
    }

    public void setCapacity(int capacity) {
        this.capacity = capacity;
    }

    // Get-set Price
    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
