package Latihan3;

class Processor {
    private String name;
    private int price;

    // Constructor
    public Processor(){
    }

    // Get-set Name
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    // Get-set Price
    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}