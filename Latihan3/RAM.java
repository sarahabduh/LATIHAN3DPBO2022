package Latihan3;

class RAM {
    private int capacity;
    private int price;

    // Constructor
    public RAM(){
    }

    // Get-set Capacity
    public int getCapacity() {
        return capacity;
    }

    public void setCapacity(int capacity) {
        this.capacity = capacity;
    }
    
    // Get-set Price
    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}